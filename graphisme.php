function typeWriter(text) {
  let i = 0;
  const speed = 50; // Vitesse de frappe en millisecondes

  function type() {
    if (i < text.length) {
      document.getElementById('text').textContent += text.charAt(i);
      i++;
      setTimeout(type, speed);
    }
  }

  type();
}

</script>

